package modelo;

/**
 *
 * @author sistemate
 */
public class NivelDeUsario {
    private String descripcion;
    private int nivel;

    public NivelDeUsario(String descripcion, int nivel) {
        this.descripcion = descripcion;
        this.nivel = nivel;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getNivel() {
        return nivel;
    }

    public void setNivel(int nivel) {
        this.nivel = nivel;
    }

    @Override
    public String toString() {
        return descripcion;
    }
}
